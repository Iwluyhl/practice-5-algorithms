print("Введите текущее время:")
# Ввод часов
h = int(input("Часы (0–23): "))

# Ввод минут
m = int(input("Минуты (0–59): "))

# Ввод секунд
s = int(input("Секунды (0–59): "))

if 0 <= h <= 23 and 0 <= m <= 59 and 0 <= s <= 59:
    print(f"Введённое время корректно: {h:02d}:{m:02d}:{s:02d}")
else:
    if not (0 <= h <= 23):
        print("  - Часы должны быть от 0 до 23")
    if not (0 <= m <= 59):
        print("  - Минуты должны быть от 0 до 59")
    if not (0 <= s <= 59):
        print("  - Секунды должны быть от 0 до 59")
