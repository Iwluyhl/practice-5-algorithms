import random

# Ответы для предсказателя
positive_answers = [
    "Да, определённо!",
    "Конечно!",
    "Всё указывает на да!",
    "Очень вероятно!",
    "Не сомневайтесь!"
]

negative_answers = [
    "Нет.",
    "Не думаю.",
    "Маловероятно.",
    "Определённо нет!",
    "Точно не сейчас."
]

neutral_answers = [
    "Может быть.",
    "Неизвестно.",
    "Спросите позже.",
    "Сейчас сложно сказать.",
    "Это зависит от обстоятельств.",
    "Попробуйте ещё раз.",
    "Время покажет.",
    "Перепроверьте свой вопрос.",
    "Ответ неоднозначен.",
    "Подумайте над этим."
]

# Объединяем все ответы
all_answers = positive_answers + negative_answers + neutral_answers

# НАЧАЛО ПРОГРАММЫ
print("=" * 50)
print("       ДОБРО ПОЖАЛОВАТЬ К ПРЕДСКАЗАТЕЛЮ!")
print("=" * 50)
print("\nЯ волшебный предсказатель.")
print("Задайте мне вопрос, и я дам вам ответ...")
print()

# Получаем вопрос от пользователя
question = input("Ваш вопрос: ").strip()

# Проверяем, что пользователь ввел вопрос
if not question:
    print("\nОшибка! Вы не задали вопрос.")
else:
    # Проверяем, заканчивается ли вопрос на вопросительный знак
    if not question.endswith("?"):
        question += "?"
    
    # Даём время на "размышление"
    print("\nПредсказатель размышляет", end="")
    for i in range(3):
        print(".", end="", flush=True)
        random.seed()  # Для переключения между вызовами
    print()
    
    # Выбираем случайный ответ
    answer = random.choice(all_answers)
    
    # Выводим результат
    print("\n" + "=" * 50)
    print(f"Вопрос: {question}")
    print(f"Ответ:  {answer}")
    print("=" * 50)

print("\nСпасибо за использование предсказателя!")

