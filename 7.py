try:
    h, m = map(int, input("Введите часы и минуты через пробел: ").split())
    if 0 <= h <= 23 and 0 <= m <= 59:
        if 5 <= h < 12:
            print("Доброе утро!")
        elif 12 <= h < 17:
            print("Добрый день!")
        elif 17 <= h < 23:
            print("Добрый вечер!")
        else:
            print("Доброй ночи!")
    else:
        print("Ошибка: некорректное время!")
except:
    print("Ошибка: введите два целых числа через пробел!")
