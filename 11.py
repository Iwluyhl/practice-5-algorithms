import random

# ВОПРОСЫ: (текст, [вариант1, вариант2, вариант3, вариант4], индекс_правильного)
questions = [
    ("Какой самый большой океан?", ["Атлантический", "Индийский", "Тихий", "Ледовитый"], 2),
    ("Столица Франции?", ["Версаль", "Париж", "Марсель", "Лион"], 1),
    ("Сколько планет в Солнечной системе?", ["8", "9", "10", "7"], 0),
    ("Элемент Au - это?", ["Серебро", "Золото", "Аргон", "Алюминий"], 1),
    ("Кто написал 'Война и мир'?", ["Достоевский", "Пушкин", "Толстой", "Лермонтов"], 2),
    ("Когда началась ВМВ?", ["1938", "1939", "1940", "1941"], 1),
    ("Кто доказал теорему Ферма?", ["Гаусс", "Риман", "Уайлс", "Эйлер"], 2),
    ("Скорость света?", ["300 000 км/с", "150 000 км/с", "500 000 км/с", "200 000 км/с"], 0),
    ("Какой художник отрезал ухо?", ["Пикассо", "Ван Гог", "Дали", "Матисс"], 1),
    ("Сторон у додекаэдра?", ["10", "12", "20", "8"], 1),
]

# ПРИЗЫ за каждый уровень
prizes = ["500", "1000", "2000", "4000", "8000", "16000", "32000", "64000", "128000", "1000000"]

# НАЧАЛО ИГРЫ
print("=== ИГРА 'О, СЧАСТЛИВЧИК!' ===")
print("Ответьте на 10 вопросов правильно - выигрывайте 1 000 000!")
print()

# Перемешиваем вопросы в случайном порядке
order = random.sample(range(len(questions)), len(questions))

# Переменная для отслеживания успеха
level = 0

# Проходим по каждому вопросу
for question_num in range(len(questions)):
    # Берем текущий вопрос
    index = order[question_num]
    text, options, correct = questions[index]
    
    # Выводим вопрос
    print(f"Вопрос {question_num + 1} (выигрыш: {prizes[question_num]})")
    print(f"{text}")
    print()
    
    # Выводим варианты ответов
    for i in range(4):
        print(f"  {i + 1}. {options[i]}")
    print()
    
    # Получаем ответ игрока (с проверкой на корректность)
    valid_input = False
    while not valid_input:
        try:
            user_answer = int(input("Ваш ответ (1-4): ")) - 1
            if 0 <= user_answer <= 3:
                valid_input = True
            else:
                print("Введите 1, 2, 3 или 4!")
        except:
            print("Введите число!")
    
    # Проверяем правильность ответа
    if user_answer == correct:
        print("✓ Правильно!\n")
        level = question_num + 1
    else:
        print(f"✗ Неправильно! Правильный ответ: {options[correct]}\n")
        break

# ИТОГИ
print("=" * 40)
if level == 10:
    print("ВЫ ПОБЕДИЛИ! ВЫИГРЫШ: 1 000 000!")
else:
    prize = prizes[level - 1] if level > 0 else "0"
    print(f"Игра окончена на вопросе {level + 1}")
    print(f"Ваш выигрыш: {prize}")
print("=" * 40)
