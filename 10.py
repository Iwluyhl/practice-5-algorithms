animals = {
    1: ("Кошка", "Мяу-мяу"),
    2: ("Собака", "Гав-гав"),
    3: ("Корова", "Мууу"),
    4: ("Лошадь", "И-го-го"),
    5: ("Овца", "Беее"),
    6: ("Курица", "Ко-ко-ко"),
    7: ("Петух", "Ку-ка-ре-ку"),
    8: ("Утка", "Кря-кря"),
    9: ("Свинья", "Хрю-хрю"),
    10: ("Лев", "Ррррр")
}

print("Выберите животное:")
for num, (name, _) in animals.items():
    print(f"{num} – {name}")

choice = int(input("Введите номер: "))

if choice in animals:
    name, sound = animals[choice]
    print(f"{name} издаёт звук: {sound}")
else:
    print("Нет животного с таким номером.")
